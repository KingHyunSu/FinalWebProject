package com.postoffice.web.dto;

public class VillageDTO {
	private String vid;
	private String vname;
	private String vadmin;
	private String vtel;
	private int vlat;
	private int vlng;
	private String vpath;
	public String getVid() {
		return vid;
	}
	public void setVid(String vid) {
		this.vid = vid;
	}
	public String getVname() {
		return vname;
	}
	public void setVname(String vname) {
		this.vname = vname;
	}
	public String getVadmin() {
		return vadmin;
	}
	public void setVadmin(String vadmin) {
		this.vadmin = vadmin;
	}
	public String getVtel() {
		return vtel;
	}
	public void setVtel(String vtel) {
		this.vtel = vtel;
	}
	public int getVlat() {
		return vlat;
	}
	public void setVlat(int vlat) {
		this.vlat = vlat;
	}
	public int getVlng() {
		return vlng;
	}
	public void setVlng(int vlng) {
		this.vlng = vlng;
	}
	public String getVpath() {
		return vpath;
	}
	public void setVpath(String vpath) {
		this.vpath = vpath;
	}
	
	
	
}
